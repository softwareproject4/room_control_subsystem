    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        // Validation
        if(jTextField1.getText()== null || jTextField1.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Device Id can't be blank");
             return ;
        }
        
          if(jTextField2.getText()== null || jTextField2.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Room name can't be null");
             return ;
        }
           if(jTextField3.getText()== null || jTextField3.getText().isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Temprature can't be null");
             return ;
        }
        
        Temprature temp = new Temprature();
        temp.setDeviceId(jTextField1.getText());
        temp.setRoomName(jTextField2.getText());
        temp.setTemprature(jTextField3.getText());
        temp.setTime(new Date().toString());
        try {
            HttpResponse<GenericResponse> postResponse = Unirest.post(Utility.url+"temprature/addDevice")
                    .header("accept", "application/json")
                    .header("Content-Type", "application/json")
                    .body(temp)
                    .asObject(GenericResponse.class);
            
            JOptionPane.showMessageDialog(rootPane, postResponse.getBody().getMessage());
           // Update row in table
           DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.addRow(new String[]{temp.getDeviceId(),temp.getRoomName(), temp.getTemprature(), temp.getTime()});
           tableModel.fireTableDataChanged();  
           // set the data to blank
           jTextField1.setText("");
           jTextField2.setText("");
           jTextField3.setText("");
           
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }

    }//GEN-LAST:event_addButtonActionPerformed

    private void updateTempBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTempBtnActionPerformed
        
          try {
                int rowIndex=jTable1.getSelectedRow();
                DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
              String deviceId=(String) tableModel.getValueAt(rowIndex, 0);
              
              String newTemp=jTextField3.getText();
              Temprature temp=new Temprature();
              temp.setDeviceId(deviceId);
              temp.setTemprature(newTemp);
                        
            HttpResponse<GenericResponse> postResponse = Unirest.post(Utility.url+"temprature/updateTemprature/")
                    .header("accept", "application/json")
                    .header("Content-Type", "application/json")
                    .body(temp)
                    .asObject(GenericResponse.class);
            JOptionPane.showMessageDialog(rootPane, postResponse.getBody().getMessage());
           // Update row in table
          tableModel.setValueAt(newTemp, rowIndex,2);
          tableModel.fireTableDataChanged();
               
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
        
    }//GEN-LAST:event_updateTempBtnActionPerformed

    private void removeDevBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeDevBtnActionPerformed
        try {
                int rowIndex=jTable1.getSelectedRow();
                 DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
              String deviceId=(String) tableModel.getValueAt(rowIndex, 0);
              System.out.println("-- Device Id "+deviceId);
                        
            HttpResponse<GenericResponse> postResponse = Unirest.delete(Utility.url+"temprature/removeDevice/"+deviceId)
                    .header("accept", "application/json")
                    .header("Content-Type", "application/json")
                    .asObject(GenericResponse.class);
            JOptionPane.showMessageDialog(rootPane, postResponse.getBody().getMessage());
           // Update row in table
         tableModel.removeRow(rowIndex);
          tableModel.fireTableDataChanged();
               
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_removeDevBtnActionPerformed


