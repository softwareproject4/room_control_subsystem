    public void updateView() {
        System.out.println("Update View Called");
      
         DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
         
        try {
            HttpResponse<GenericResponse> allDevices = Unirest.get(Utility.url+"temprature/getAllDevice")
                    .header("accept", "application/json")
                    .header("Content-Type", "application/json")
                    .asObject(GenericResponse.class);
            
             List  allDevs=(List)allDevices.getBody().getData();
             
               System.out.println("---- "+allDevs.size());
               
               for (Object allDev : allDevs) {
                   System.out.println("-------------- "+allDev.toString());
                   HashMap<String,String> dev=(HashMap<String,String>)allDev;
                 
                 tableModel.addRow(new String[]{dev.get("deviceId"),dev.get("roomName"), dev.get("temprature"), dev.get("time")});
                  
            }
                tableModel.fireTableDataChanged();
            
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
        
       

    }

